const mongoose = require("mongoose");

// Déclaration du schéma du modèle MongoDB
const productSchema = new mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
      trim: true,
    },
    slug: {
      type: String,
      required: true,
      unique: true,
      lowercase: true,
    },
    description: {
      type: String,
      required: true,
    },
    price: {
      type: Number,
      required: true,
    },
    category: {
      type: String,
      required: true,
    },
    brand: {
      type: String,
      required: true,
    },
    quantity: {
      type: Number,
      required: true,
    },
    sold: {
      type: Number,
      default: 0,
    },
    images: [
      mongoose.Schema.Types.Mixed // Allow both strings and objects
    ],
    color: [{ type: mongoose.Schema.Types.ObjectId, ref: "Color" }], // Correction ici
    tags: [String], // Changed to array of strings
    ratings: [
      {
        star: Number,
        comment: String,
        postedby: { type: mongoose.Schema.Types.ObjectId, ref: "User" },
      },
    ],
    totalrating: {
      type: String,
      default: 0,
    },
  },
  { timestamps: true }
);

// Export du modèle
module.exports = mongoose.model("Product", productSchema);
